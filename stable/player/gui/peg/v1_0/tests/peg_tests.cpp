// (epg,9/4/2001): atomic test fn

void peg_test( cyg_uint32 data ) {
    PegMessageQueue *pMsgQueue = new PegMessageQueue();
	PegThing::SetMessageQueuePtr(pMsgQueue);

	PegScreen *pScreen = CreatePegScreen();
	PegThing::SetScreenPtr(pScreen);

	PegRect Rect;
	Rect.Set(0, 0, pScreen->GetXRes() - 1, pScreen->GetYRes() - 1);
	PegPresentationManager *pPresentation = new PegPresentationManager(Rect);
	PegThing::SetPresentationManagerPtr(pPresentation);
	pScreen->GenerateViewportList(pPresentation);

	PegRect rtScreen;
	rtScreen.Set(0, 0, LCD_WIDTH-1, LCD_HEIGHT-1);
	g_pDAReceiverMaster = new CDAReceiverMaster(rtScreen);
	pPresentation->Add(g_pDAReceiverMaster);
	g_pMainWindow = new CInsertDiskScreen(g_pDAReceiverMaster);
	g_pDAReceiverMaster->Add(g_pMainWindow);

	PegMessage NewMessage(PM_DRAW);
	NewMessage.pTarget = pPresentation;
	pMsgQueue->Push(NewMessage);

	pScreen->Invalidate(pPresentation->mReal);
    pPresentation->Invalidate(pPresentation->mReal);

   	pPresentation->Execute();
}

